#include <iostream>
#include <algorithm>

using namespace std;

int main(int argc, const char * argv[])
{
    // max，min
    int maxI = 3;
    int maxJ = 4;
    cout << "较大值为：" << max(maxI,maxJ) << endl;
    cout << "较小值为：" << min(maxI,maxJ) << endl;






    return 0;
}





